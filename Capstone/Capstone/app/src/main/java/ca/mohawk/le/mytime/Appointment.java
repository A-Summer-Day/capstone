package ca.mohawk.le.mytime;


public class Appointment {
    String name;
    String doctor;
    String address;
    String date;
    String time;

    public Appointment(String name, String doctor, String address, String date, String time) {
        this.name = name;
        this.doctor = doctor;
        this.address = address;
        this.date = date;
        this.time = time;
    }

}
